package src.Session3;

public class WhileLoopExample {
    public static void main(String[] args) {
        // print numbers from 1 to 10
        int i = 1;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }

        // print numbers from 10 to 1
        i = 10;
        while (i >= 1) {
            System.out.println(i);
            i--;
        }
    }
}
