package src.Session3;

public class ForLoopExample {
    public static void main(String[] args) {

        // print numbers from 1 to 10
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        // print numbers from 10 to 1
        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
